public class Hw8 {
        public static int addition(int a, int b) {
            return a + b;
        }

        public static int subtraction(int a, int b) {
            return a - b;
        }

        public static void main(String[] args) {
            int x = 10;
            int y = 6;
            System.out.println("Результат додавання: " + addition(x, y));
            System.out.println("Результат віднімання: " + subtraction(x, y));
        }
    }

